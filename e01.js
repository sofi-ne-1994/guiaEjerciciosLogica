/*1. Se le solicita al usuario que ingrese los tres lados de un triángulo. Realice el algoritmo
para calcular e informar el perímetro del triángulo.*/

let lado1 = parseInt(prompt("Ingrese el primer lado del triangulo"))
let lado2 = parseInt(prompt("Ingrese el segundo lado del triangulo"))
let lado3 = parseInt(prompt("Ingrese el tercero lado del triangulo"))

let perimetro = lado1 + lado2 + lado3

alert("El perimetro del triangulo es " + perimetro)
