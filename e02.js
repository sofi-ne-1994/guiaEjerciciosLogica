/*2. Se le solicita al usuario que ingrese el lado de un cuadrado. Realice el algoritmo para
calcular e informar el perímetro y el área del cuadrado.*/

let ladocuadrado = parseInt(prompt("Ingrese el lado del cuadrado"))
let perimetroCuadrado = ladocuadrado * 4
let areacuadrado = ladocuadrado * 2
alert("El perímetro del cuadrado es " + perimetroCuadrado)
console.log("El perímetro del cuadrado es " + perimetroCuadrado)
alert("El área del cuadrado es " + areacuadrado)
console.log("El área del cuadrado es " + areacuadrado)
