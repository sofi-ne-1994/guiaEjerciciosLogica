/*10. Se le solicita al usuario que ingrese 2 valores numéricos. Realice el algoritmo para
calcular e informar la suma de estos valores y la multiplicación de estos.*/

let num1 = parseInt (prompt("Por favor, ingresa el primer numero"))
let num2 = parseInt (prompt("Por favor, ingresa el segundo numero"))
let suma = num1 + num2
let producto = num1 * num2
alert("El resultado de la suma es  " + suma)
alert("El producto de la multiplicación es " + producto)