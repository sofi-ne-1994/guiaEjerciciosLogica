/*15. Realizar un algoritmo (utilizar la estructura else if) que pida el color favorito al usuario y evalue, si
el color es rojo, azul o amarillo, en ese caso, devolver “Este color es primario + nombreColor” y
sino “El color ingresado no es primario”.*/


let nombreColor = prompt("Ingrese su color favorito")

if(nombreColor == "rojo"){
    alert(`Tu color favorito es primario. Color ${nombreColor}`)
}else if(nombreColor == "amarillo"){
    alert(`Tu color favorito es primario. Color ${nombreColor}`)
}else if(nombreColor == "azul"){
    alert(`Tu color favorito es primario. Color ${nombreColor}`)
}else{
    alert(`El color ingresado no es primario. Color ${nombreColor}`)
}
